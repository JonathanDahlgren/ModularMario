  public getLevel(currentLevel: number): any {

    let cellUnit = this.GA.w / this.levels[currentLevel].layout.length;
    let x = this.GA.x;
    let y = this.GA.y;
    let w = cellUnit;
    let h = cellUnit;
    const levelObjects: Array<LevelObject> = [];
    const levelAssets: Array<LevelObject> = [];

    for (let i = 0; i < this.levels[currentLevel].layout.length; i++) {
      for (let j = 0; j < this.levels[currentLevel].layout[i].length; j++) {
        switch (this.levels[currentLevel].layout[i][j]) {
          // case 0: {
          //   let emptySpace = new EmptySpace(x, y, w, h);
          //   levelObjects.push(emptySpace);
          //   break;
          // }
          case 1:
            let block = new Block(x, y, w, h);
            levelObjects.push(block);
            break;
          case 2:
            let startBlock = new StartBlock(x, y, w, h);
            levelObjects.push(startBlock);
            break;
          case 3:
            let finishBlock = new FinishBlock(x, y, w, h);
            levelObjects.push(finishBlock);
            break;
        }
        x += cellUnit;
      }
      x = this.GA.x;
      y += cellUnit;
    }

    for(let i = 0; i < this.levels[currentLevel].assets.length; i++) {
        switch (this.levels[currentLevel].assets[i]) {
            case 1:
              let ladder = new Ladder(x, y, w, h);
              levelAssets.push(ladder);                
              break;                
            case 2:
              let log = new Log(x, y, w, h);
              levelAssets.push(log);               
              break;                
            case 3:
              let stone = new Stone(x, y, w, h);
              levelAssets.push(stone);              
              break;                
        }
    }
    return new Level(levelObjects, levelAssets);
  }
